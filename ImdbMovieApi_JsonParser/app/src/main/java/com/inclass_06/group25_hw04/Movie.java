package com.inclass_06.group25_hw04;

import android.os.Parcel;
import android.os.Parcelable;

import org.json.JSONException;
import org.json.JSONObject;

import java.io.Serializable;

/**
 * Created by Krunal on 22-02-2016.
 */
public class Movie implements Serializable {

    String title, imdbID, type, poster, released, genre, director, actors, plot;
    double imdbRating;

    public String getReleased() {
        return released;
    }

    public void setReleased(String released) {
        this.released = released;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getDirector() {
        return director;
    }

    public void setDirector(String director) {
        this.director = director;
    }

    public String getActors() {
        return actors;
    }

    public void setActors(String actors) {
        this.actors = actors;
    }

    public String getPlot() {
        return plot;
    }

    public void setPlot(String plot) {
        this.plot = plot;
    }

    public double getImdbRating() {
        return imdbRating;
    }

    public void setImdbRating(double imdbRating) {
        this.imdbRating = imdbRating;
    }

    int year;

    public static Movie createMovie(JSONObject movieJsonObj) throws JSONException
    {
        Movie movie = new Movie();

        movie.setTitle(movieJsonObj.getString("Title"));
        movie.setImdbID(movieJsonObj.getString("imdbID"));
        movie.setPoster(movieJsonObj.getString("Poster"));
        movie.setType(movieJsonObj.getString("Type"));
        movie.setYear(movieJsonObj.getInt("Year"));

        return movie;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getImdbID() {
        return imdbID;
    }

    public void setImdbID(String imdbID) {
        this.imdbID = imdbID;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPoster() {
        return poster;
    }

    public void setPoster(String poster) {
        this.poster = poster;
    }
}
